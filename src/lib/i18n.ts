export function useT(lang: Lang) {
  return (key: string, vars?: Record<string, string>) => {
    let s = STR[lang]?.[key] ?? STR.EN[key] ?? key;
    if (vars) for (const [k, v] of Object.entries(vars)) s = s.split(`{${k}}`).join(v);
    return s;
  };
}
